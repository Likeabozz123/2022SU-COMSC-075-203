package QuestionOne;

public class Date {

    int day;
    int month;
    int year;

    public Date() {
        this.day = 1;
        this.month = 1;
        this.year = 1111;
    }

    public Date(int day, int month, int year) {
        this.day = day;
        this.month = month;
        this.year = year;
    }
}
